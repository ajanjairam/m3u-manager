    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    NAME TEXT NOT NULL,
    PLAYLIST_ID INTEGER,
    GROUP_ID INTEGER,
    INCLUDE TEXT,
    EXCLUDE TEXT,
    ACTIVE INTEGER DEFAULT 1,
    FOREIGN KEY (PLAYLIST_ID) REFERENCES PLAYLIST(ID)
        ON DELETE SET NULL
        ON UPDATE CASCADE,
    FOREIGN KEY (GROUP_ID) REFERENCES CGROUP(ID)
        ON DELETE SET NULL
        ON UPDATE CASCADE
);

